public class Person {

    private String run;
    private String jump;


    public Person() {
        this.run = run;
        this.jump = jump;
    }


    public String getRun() {
        return run;
    }

    public void setRun(String run) {
        this.run = run;
    }

    public String getJump() {
        return jump;
    }

    public void setJump(String jump) {
        this.jump = jump;
    }
}
